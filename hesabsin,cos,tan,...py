import math
a=int(input("enter first number:"))
b=int(input("enter second number:"))
c=a+b
print("Total:",c)
print ("SIN:",math.sin(math.radians(c)))
print ("COS:",math.cos(math.radians(c)))
print ("TAN:",math.tan(math.radians(c)))
R=math.sqrt(c)
print("RADICAL:",R)
print("FACTORIAL:",math.factorial(c))
